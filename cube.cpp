void Cube_Uninit()
{
	if (g_pVertexBuffer)
	{
		g_pVertexBuffer->Release();
		g_pVertexBuffer = NULL;
	}
	if (g_pIndexBuffer)
	{
		g_pIndexBuffer->Release();
		g_pVertexBuffer = NULL;
	}
}

void Cube_Update()
{
	frame++;
	rad += 0.05f;
	kakudai = sin(rad) / 2;
}
void Cube_Draw()
{
	LPDIRECT3DDEVICE9 pDevice = MyDirect3D_GetDevice();  //Deviceの取得

	CubeVertex *pv;
	g_pVertexBuffer->Lock(0, 0, (void**)&pv, D3DLOCK_DISCARD);
	//                      ↑全部       ↑仮想アドレス

	for (int i = 0; i <= 121; i++)
	{
		pv[i].uv.y -= 0.005f;
	}

	for (int i = 22; i <= 32; i++)
	{
		pv[i].position.y = kakudai;
	}

	g_pVertexBuffer->Unlock(); //Unlockをしたら仮想アドレスは使えない

	//ワールド変換行列
	D3DXMATRIX mtxWorld, mtxRotationY , mtxRotationX, mtxTranslation, mtxScaling, mtxTranslation2;

	//何もしない行列（平行移動・回転・拡大）
	//D3DXMatrixIdentity(&mtxWorld);  //単位行列にする

	Camera_Update();

	pDevice->SetFVF(FVF_CUBE);
	pDevice->SetTexture(0, Texture_GetTexture(g_texid));
	
	D3DXMatrixTranslation(&mtxTranslation, 0.0f, 0.5f, 0.0f);
	mtxWorld = mtxTranslation;
	pDevice->SetTransform(D3DTS_WORLD, &mtxWorld); //デバイスにワールド変換行列を設定

	pDevice->SetStreamSource(0, g_pVertexBuffer, 0, sizeof(CubeVertex));
	pDevice->SetIndices(g_pIndexBuffer);
	pDevice->DrawIndexedPrimitive(D3DPT_TRIANGLELIST, 0, 0, 400, 0, 200);
}